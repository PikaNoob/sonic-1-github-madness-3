sprite_entry	macro	size, xCoord, yCoord, tileID, palLine, priority, xflip, yflip
		dc.w	\yCoord
		dc.b	\size,0
		dc.w	(((\priority&1)<<15)|((\palLine&3)<<13)|((\yflip&1)<<12)|((\xflip&1)<<11))+\tileID
		dc.w	\xCoord
		endm

